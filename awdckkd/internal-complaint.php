								<h5>Formation of Internal Complaints Committee (WPC) </h5>
								<p>As per the provision of the Section 4 (20 of the Sexual Harassment of Women at the Workplace (Prevention, Prohibition and Redressal) Act 2013, Aditya Degree College, kakinada has formed the Internal Complaint Committee (WPC) in the College to look into the matters pertaining to the Gender sensitive issues and curb atrocities against women.</p> 
								<h5>Organizational Structure</h5>
								<p>
									The organizational structure of Internal Complaint Committee (WPC) in the College is as follows:  The Chair Person is at the helm of affairs organizing the entire individual Sub-Committees.  The members of the ICC are allotted specific duties for organizational ease
								</p>
								<h5>Legal Aspects</h5>
								<p>
									There are several stringent rules, regulations and guidelines prescribed for mandatory implementation in the educational institutions against sexual harassment of women at work place under (Prevention, Prohibition and Redressal Act, 2013). The following is the prescribed code of conduct as per the ACT in order to curb the serious criminal offence of sexual harassment:<br>Sexual harassment can be broadly defined as incidents of direct physical contact, demand for sexual favors, or implied
								</p> 
								<p>  
								<ul>
								  <li>Eve- teasing,</li>
								  <li>unsavory remark,</li> 
								  <li>jokes causing or likely to cause awkwardness or embarrassment,</li>
								  <li>innuendos and taunts,</li>
								  <li>gender based insults or sexiest remark,</li>
								  <li>unwelcome sexual overtone in any manner such as over telephone ,</li>
								  <li>touching or brushing against any part of the body and the like,</li>
								  <li>displaying filthy pornographic or other offensive or derogatory pictures or gestures,</li>
								  <li>forcible physical touch or molestation,</li>
								  <li>Physical confinement against one's will and any other act likely to violate one's privacy or any hostile or intimidating act.</li>
								</ul>
								</p> 
								<h5>Constitution: (WPC)</h5>
								<p>The Committee shall consist of following members:</p> 
								<ul>
								  <li>The Principal as the Chairman</li>
								  <li>Convener of the Committee nominated by the Principal as the Member Secretary - Ex-officio</li> 
								  <li>One senior female faculty from each Department to be nominated by HODs</li>
								  <li>One external member form the society</li>
								  <li>Two girl students from each Department to be nominated by HODs</li>
								  <li>The number of members can be increased equally by adding nominees of the Principal.</li>
								  <li>The total number of members of the Women Protection Cell however shall not exceed 16.</li> 
								</ul>
								<p>
									The purpose of this Committee is to provide protection to the women in professional environment and prevention, redressed of complaints of sexual harassment.
								</p>
								<h5>Working Rules for Internal Complaints Committee (WPC):</h5>
								<p>
									In pursuance of the Sexual Harassment of Women at Workplace (Prevention, Prohibition and Redressal) Act 2013 and the Rules framed there under, Aditya Degree College, Kakinada adopts the procedures detailed hereunder for processing the complaints filed to the Internal Complaints Committee (ICC) and adequately address the issues so that natural justice and fair play prevail in disposing off all the complaints. The ICC reserves the right to modify the procedures under exceptional cases and conditions:
								</p> 
								<ul>
								  <li>An aggrieved woman needs to lodge a formal written complaint within a period of fifteen days in instances of sexual harassment.</li>
								  <li>The received complaint shall be forwarded to the Presiding Officer immediately, who will notify the Committee members within three days.</li> 
								  <li>The Committee shall decide the jurisdiction and issue notices within 7 working days to get written explanations within 10 days.</li>
								  <li>Before initiation of any action, the Committee may work out reconciliation between the offender and the aggrieved and record the same extending copies to both the parties.</li>
								  <li>If conciliation is found to be not feasible, notice will be issued to both parties for hearing.</li>
								  <li>The Committee shall conduct an enquiry into the complaint in accordance with the provisions of the rules covering sexual harassment as misconduct.</li>
								  <li>The Committee shall provide reasonable opportunity to the aggrieved woman and the respondent for presenting and defending her/his case.</li> 
								  <li>After examining all the available evidences and accusations and defenses, the Committee shall arrive at the intensity of the crime and makes recommendations to the Principal regarding the scale of punishment.</li> 
								</ul>
								<h5>Constitution of the Enquiry Committee:</h5>
								<p>
									The Enquiry Committee is a part of the Internal Committee with the specific function of conducting the enquiries and making recommendations. The following parameters could be followed in the constitution of the Enquiry Committee. The chair person, three   members from the faculty, two women and a man shall be the regular members.
								</p>
								<h5>The Process of Inquiry:</h5> 
								<ul>
								  <li>In general the following is the procedure of inquiry.</li>
								  <li>A notice shall be issued to both parties asking them to make written explanations within 48 hours.</li> 
								  <li>The process of oral hearing starts after receiving the written explanations, including witnesses.</li>
								  <li>The enquiry commission is entitled to ask probing questions to arrive at the facts.</li>
								  <li>Additional witnesses may also be summoned.</li>
								  <li>No outsider will be allowed during the process unless warranted.</li>
								  <li>The decision of the committee will be final and binding.</li>
								</ul>
