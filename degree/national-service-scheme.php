							<div class="tab-pane fade" id="action" role="tabpanel" aria-labelledby="home-tab">
								<h5>Roles &amp; Responsibilities of Coordinator </h5>
<ul type="disc">
  <li>He/she will plan the NSS regular       activities and special camping programme as Programme guidelines issued by       the Government of India and Programme Coordinator of the concerned university.</li>
  <li>The Programme officer will ensure that       NSS volunteers complete the prescribed hours in regular activities and       participate in special camping programme as per requirements. The       orientation of the NSS volunteers should be conducted in a befitting       manner and 20 hours should be devoted to orient the NSS volunteers.</li>
  <li>He/she will divide the NSS unit into       different groups and assign the definite task and targets and projects to       each group.</li>
  <li>He/she will supervise the working of       NSS volunteers.</li>
  <li>He/she will ensure that the basic aims       of NSS Programme i.e. personality development of NSS volunteers,       interaction of the different classes of society take place harmoniously       and the NSS volunteers and community are benefited from the activities of       NSS unit.</li>
  <li>He/she will maintain the necessary       records and registers prescribed by Government of India and Programme       Coordinator of the university.</li>
  <li>He/she will be responsible for the       equipment and stores purchased out of NSS funds. He/she will hand over the       charge of NSS equipment and stores to his/her successor at the end of       his/her tenure.</li>
  <li>He/she will spend the NSS grants as       per administrative and financial directives of the Government of India and       NSS Programme Coordinator.</li>
  <li>He/she will depute NSS volunteers for       participation in various programmes sponsored by the Department of Youth       Affairs &amp; Sports, Government of India such as National Integration       Camps, Adventure Programmes, Republic Day Parade and any other function       organized by the NSS Regional Centre and concerned University.</li>
  <li>He/she will submit the reports to the       NSS Regional Centre periodically, Programme Coordinator, NSS State Liaison       Officer and TORC/TOC.</li>
  <li>He/she will ensure the submission of       accounts in time. The accounts may be got audited from a chartered       accountant or departmental auditor along with the utilization certificate.</li>
  <li>He/she will liaise with the officials       of the department for NSS projects and activities. He/she will also       coordinate with the voluntary organizations working in the field of       community development in general and youth work in particular.</li>
  <li>He/she will       convene the meeting of the college advisory committee in consultation with       the Principal of the institution</li>
</ul>
