							<div class="tab-pane fade show active" id="about" role="tabpanel" aria-labelledby="home-tab">
								<p>The overall aim of National  Service Scheme is to give an extension dimension to the higher education system  and orient the student youth to community service while they are studying in  educational institution. The reason for the formulation of this objective is  the general realization that the college students have a tendency to get  alienated from the village/slum masses which constitute the majority of the  population of the country. The educated youth who are expected to take the  reins of administration in future are found to be unaware of the problems of  the village/slum community and in certain cases are indifferent towards their  needs and problems. Therefore it is necessary to arouse the social conscience  of the students, and to provide them an opportunity to work with the people in  the villages and slums. It is felt that their interaction with the common  villagers and slum dwellers will expose them to the realities of life and bring  about a change in their social perception.</p>

							</div>
							<div class="tab-pane fade" id="roles" role="tabpanel" aria-labelledby="profile-tab">
								<h5>The broad objectives of NSS are  to:</h5>
<ul type="disc">
  <li>Understand the community in which they       work</li>
  <li>Understand themselves in relation to       their community;</li>
  <li>Identify the needs and problems of the       community and involve them in problem solving process;</li>
  <li>Develop among themselves a sense of       social and civic responsibility;</li>
  <li>Utilize their knowledge in finding       practical solution to individual and community problems;</li>
  <li>Develop competence required for group       living and sharing of responsibilities;</li>
  <li>Gain skills in mobilizing community participation;</li>
  <li>Acquire leadership qualities and       democratic attitude;</li>
  <li>Develop capacity to meet emergencies       and natural disasters and</li>
  <li>Practice       national integration and social harmony.</li>
</ul>
							</div>
							<div class="tab-pane fade" id="complaint" role="tabpanel" aria-labelledby="home-tab">
								<p>The motto or watchword of the  National Service Scheme is: &lsquo;NOT ME BUT YOU&rsquo;. This reflects the essence of  democratic living and upholds the need for selfless service and appreciation of  the other person&rsquo;s point of view and also to show consideration for fellow  human beings. It underlines that the welfare of an individual is ultimately  dependent on the welfare of society on the whole. Therefore, it should be the  aim of the NSS to demonstrate this motto in its day-to-day programme.<br>
  The  National Service Scheme was started to establish a meaningful linkage between  the campus and the community. Mahatma Gandhi, the Father of the Nation, had  recognized that the country could not progress in a desired direction until the  student youth were motivated to work for the upliftment of the  villages/community. For Gandhiji the villages, where majority of the population  lived, represent the country i.e. India. Therefore, for the national  reconstruction and national resurgence it was deemed fit that the students and  teachers should be properly sensitized and utilized for strengthening the  Indian society as a whole with particular emphasis on rural community.  Therefore, student youth, teachers and the community are considered the three  basic components of the National Service Scheme.</p>
