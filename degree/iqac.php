							<strong> <u> Objectives </u> </strong>
							<h6> The primary aim of IQAC is: </h6>
							<ul> 
								<li>
									To develop a system for conscious, consistent and catalytic action to improve the academic 	and administrative performance of the institution. To promote measures for institutional functioning towards quality enhancement through internalization of quality culture and institutionalization of best practices.
								</li>
							</ul> 
							<h6>Functions of the IQAC are:</h6>
							<ul> 
								<li>
									Development and application of quality benchmarks for various academic and administrative activities of the institution.
								</li>
								<li>
									Collection and analysis of feedback from all stakeholders on quality related institutional processes.
								</li>
								<li>
									Organization of inter and intra institutional workshops, seminars on quality related themes and promotion of quality circles.
								</li>
								<li>
									Documentation of various programmes/activities leading to quality improvement.
								</li>
								<li>
									Periodical conduct of Academic and Administrative Audit and its follow-up
								</li>
							</ul>  
